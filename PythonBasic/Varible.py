## 变量命名规则
# 只能由字母、_、数字构成
# 变量名不能以数字开头
# 不能使用python关键字
# 少用拼音，多用英文
# 变量名区分大小写

k_yue = "K.Yue"

# 下滑线命名法：1 字母全部小写。2.不同单词之间用下划线连接

user_name = "K.Yue"

# 驼峰命名法：单词用首字母大写分割

UserName = "K.Yue"
